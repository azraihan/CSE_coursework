SELECT
    CASE
        WHEN SALARY BETWEEN 0 AND 4999 THEN 'Group 1: 0 to <5K'
        WHEN SALARY BETWEEN 5000 AND 9999 THEN 'Group 2: 5K to <10K'
        WHEN SALARY BETWEEN 10000 AND 14999 THEN 'Group 3: 10K to <15K'
        ELSE 'Other'
    END AS SALARY_GROUP,
    COUNT(*) AS EMPLOYEE_COUNT
FROM
    EMPLOYEES
GROUP BY
    CASE
        WHEN SALARY BETWEEN 0 AND 4999 THEN 'Group 1: 0 to <5K'
        WHEN SALARY BETWEEN 5000 AND 9999 THEN 'Group 2: 5K to <10K'
        WHEN SALARY BETWEEN 10000 AND 14999 THEN 'Group 3: 10K to <15K'
        ELSE 'Other'
    END;
   
   
