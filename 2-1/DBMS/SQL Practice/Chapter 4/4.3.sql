SELECT
    EXTRACT(YEAR FROM HIRE_DATE) AS HIRE_YEAR,
    JOB_ID,
    COUNT(*) AS TOTAL_EMPLOYEES_HIRED
FROM
    EMPLOYEES
GROUP BY
    EXTRACT(YEAR FROM HIRE_DATE),
    JOB_ID
ORDER BY
    HIRE_YEAR,
    JOB_ID;
    
